package beans;

public class Recherche {
    int idLivre;
    String titreLivre;
    int idExemplaire;
    String typeLivre;
    String themeLivre;
    String domaineLivre;

    public int getIdLivre() {
        return idLivre;
    }

    public void setIdLivre(int idLivre) {
        this.idLivre = idLivre;
    }

    public String getTitreLivre() {
        return titreLivre;
    }

    public void setTitreLivre(String titreLivre) {
        this.titreLivre = titreLivre;
    }

    public int getIdExemplaire() {
        return idExemplaire;
    }

    public void setIdExemplaire(int idExemplaire) {
        this.idExemplaire = idExemplaire;
    }

    public String getTypeLivre() {
        return typeLivre;
    }

    public void setTypeLivre(String typeLivre) {
        this.typeLivre = typeLivre;
    }

    public String getThemeLivre() {
        return themeLivre;
    }

    public void setThemeLivre(String themeLivre) {
        this.themeLivre = themeLivre;
    }

    public String getDomaineLivre() {
        return domaineLivre;
    }

    public void setDomaineLivre(String domaineLivre) {
        this.domaineLivre = domaineLivre;
    }


}
