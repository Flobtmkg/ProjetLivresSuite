package ServicesBeans;

public class Notation {
    private int idNotation;
    private int idUtilisateur;
    private int idLivre;
    private double valueNotation;

    public int getIdNotation() {
        return idNotation;
    }

    public void setIdNotation(int idNotation) {
        this.idNotation = idNotation;
    }

    public int getIdUtilisateur() {
        return idUtilisateur;
    }

    public void setIdUtilisateur(int idUtilisateur) {
        this.idUtilisateur = idUtilisateur;
    }

    public int getIdLivre() {
        return idLivre;
    }

    public void setIdLivre(int idLivre) {
        this.idLivre = idLivre;
    }

    public double getValueNotation() {
        return valueNotation;
    }

    public void setValueNotation(double valueNotation) {
        this.valueNotation = valueNotation;
    }

}
